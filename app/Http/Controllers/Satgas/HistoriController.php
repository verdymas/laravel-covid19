        ->join('historiskt AS h', 'b.id_his', 'h.id_his')
        ->join('warga AS w', 'h.nik_wrg', 'w.nik_wrg')
        ->join('kk' , 'kk.id_kk', 'b.id_kk')
        ->where('kk.id_adm', auth()->guard('satgas')->user()->id_adm)
        ->get();

        return view('satgas.histori.index', compact('data'));
    }

